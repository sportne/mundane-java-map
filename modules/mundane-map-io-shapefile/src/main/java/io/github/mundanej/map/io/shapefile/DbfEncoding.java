package io.github.mundanej.map.io.shapefile;

/** Closed set of character encodings supported by the Level 1 DBF profile. */
public enum DbfEncoding {
    /** UTF-8. */
    UTF_8,

    /** ISO/IEC 8859-1. */
    ISO_8859_1,

    /** Windows code page 1252. */
    WINDOWS_1252,

    /** IBM OEM code page 437. */
    IBM437,

    /** IBM OEM code page 850. */
    IBM850
}
