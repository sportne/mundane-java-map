package io.github.mundanej.map.api;

/** Compass position of a point label outside its translated marker bounds. */
public enum PointLabelPosition {
    /** North. */
    N,
    /** North-east. */
    NE,
    /** East. */
    E,
    /** South-east. */
    SE,
    /** South. */
    S,
    /** South-west. */
    SW,
    /** West. */
    W,
    /** North-west. */
    NW
}
