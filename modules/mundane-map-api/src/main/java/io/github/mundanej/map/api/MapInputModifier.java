package io.github.mundanej.map.api;

/** Toolkit-neutral keyboard modifiers accompanying a map-tool event. */
public enum MapInputModifier {
    /** Shift modifier. */
    SHIFT,
    /** Control modifier. */
    CONTROL,
    /** Alt modifier. */
    ALT,
    /** Platform command/meta modifier. */
    META,
    /** Alternate-graphics modifier. */
    ALT_GRAPH
}
