package io.github.mundanej.map.api;

/** The normalized marker silhouettes supplied by the core module. */
public enum BuiltInMarker {
    /** A cubic-curve circle. */
    CIRCLE,
    /** A square. */
    SQUARE,
    /** An upward-pointing triangle. */
    TRIANGLE,
    /** A diamond. */
    DIAMOND,
    /** An orthogonal cross. */
    CROSS,
    /** A diagonal X. */
    X,
    /** A five-point star. */
    STAR,
    /** A right-pointing arrow. */
    ARROW
}
