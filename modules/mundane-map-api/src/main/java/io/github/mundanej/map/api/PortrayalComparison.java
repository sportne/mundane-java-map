package io.github.mundanej.map.api;

/** Closed comparison operations for portrayal predicates. */
public enum PortrayalComparison {
    /** Equal canonical values. */
    EQUAL,
    /** Unequal canonical values. */
    NOT_EQUAL,
    /** Strictly less than. */
    LESS_THAN,
    /** Less than or equal. */
    LESS_THAN_OR_EQUAL,
    /** Strictly greater than. */
    GREATER_THAN,
    /** Greater than or equal. */
    GREATER_THAN_OR_EQUAL
}
